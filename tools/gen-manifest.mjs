// Regenerates src/levels/index.js from the src/levels/*.json directory.
// The level list is DIRECTORY-DRIVEN: adding a level = drop a JSON file
// (with an `order` field) and re-run this (the editor's save-as-new does it
// automatically through the dev-server middleware).
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

export function genManifest(dir = "src/levels") {
  const files = readdirSync(dir).filter((f) => f.endsWith(".json"));
  const entries = files.map((f) => {
    const data = JSON.parse(readFileSync(join(dir, f), "utf8"));
    return { file: f, id: data.id, order: data.order ?? 999 };
  }).sort((a, b) => a.order - b.order);
  const lines = entries.map((e, i) => `import L${i} from "./${e.file}" with { type: "json" };`);
  const mod = `// GENERATED by tools/gen-manifest.mjs — do not edit by hand.
// The level list is the src/levels/ directory; order comes from each
// level's \`order\` field. Run \`npm run levels\` after adding a file.
${lines.join("\n")}

export const LEVELS_DATA = [${entries.map((_, i) => `L${i}`).join(", ")}];
`;
  writeFileSync(join(dir, "index.js"), mod);
  return entries.map((e) => e.id);
}

if (import.meta.url === `file://${process.argv[1]}`) {
  const ids = genManifest();
  console.log(`manifest: ${ids.length} levels — ${ids.join(", ")}`);
}
